#Ta có một danh sách 3 loại quả:
fruits = ["Apple", "Peach", "Pear"]
#Sử dụng vòng lặp in ra từng loại quả trong đó
#Trong Python, khác với các ngôn ngữ lập trình khác, ta không cần sử dụng vòng lặp for quá cầu kỳ và cần khai báo nhiều tham số
#Tuy nhanh và tiện nhưng sẽ dễ gây ra sự nhầm lẫn nếu mã của bạn là nhiều dòng code lớn
for fruit in fruits:
    print (fruit)
    print (fruit + " Pie")
#Khi chúng ta không sử dụng tab nữa tức là chúng ta đã thoát khỏi vòng lặp
print(fruits)
